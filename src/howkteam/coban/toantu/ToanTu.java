package howkteam.coban.toantu;

public class ToanTu {
    public static void main(String[] args) {
        int a = 1;
        //In rồi mới tăng
        System.out.println(a++);
        // Tăng rồi mới in
        System.out.println(++a);
//         Cộng với 1 và in ra rồi mới tăng
        System.out.println(a+++1);
        //Tăng rồi mới cộng 1 và in ra
        System.out.println(++a+1);
    }
}
